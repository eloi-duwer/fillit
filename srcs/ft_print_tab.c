#include "../fillit.h"

void	ft_print_tab(void)
{
	int x;
	int y;

	x = 0;
	g_size[0]--;
	while (x < g_size[0] + 1)
	{
		y = 0;
		while (y < g_size[0] + 1)
		{
			write(1, &(g_save[x][y]), 1);
			y++;
		}
		write(1, "\n", 1);
		x++;
	}
}
